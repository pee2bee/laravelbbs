    {
        //展示分类下的帖子

        $posts = Post::withOrder(\request('order'))
                     ->with('user','category')
                     ->where('category_id','=',$category->id)->paginate(15);
        $user = new User();
        $active_users = $user->addActiveUser();

        return view('posts.index',compact('posts','category','active_users'));
    }

